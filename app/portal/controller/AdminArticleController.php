     *     'remark' => '文章发布',
     *     'param'  => ''
     * )
     */
    public function publish()
    {
        $param           = $this->request->param();
        $portalPostModel = new PortalPostModel();
        if (isset($param['ids'])) {
            $ids = $this->request->param('ids/a');
            $area = Db::name("area")->alias("a")->join("portal_post b","a.id=b.area_id","LEFT")->where("b.id=".$ids[0])->field("a.*")->find();
            if (substr_count($area["path"],'-')<=2) {
                $this->success("非村庄地区文章不需要审核！", 'AdminArticle/index');
            }
            if (isset($param["yes"])) {

                $portalPostModel->where(['id' => ['in', $ids]])->update(['post_status' => 1, 'published_time' => time()]);

                $this->success("审核成功！", 'AdminArticle/index');
            }

            if (isset($param["no"])) {

                $portalPostModel->where(['id' => ['in', $ids]])->update(['post_status' => 0,'published_time'=>0]);

                $this->success("取消审核成功！", 'AdminArticle/index');
            }
        }else{
            $this->error("请选择需要审核的文章！", 'AdminArticle/index');
        }

    }

