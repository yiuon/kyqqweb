    /**
     * 文章删除
     * @adminMenu(
     *     'name'   => '文章删除',
     *     'parent' => 'index',
     *     'display'=> false,
     *     'hasView'=> false,
     *     'order'  => 10000,
     *     'icon'   => '',
     *     'remark' => '文章删除',
     *     'param'  => ''
     * )
     */
    public function delete()
    {
        $param           = $this->request->param();
        $portalPostModel = new PortalPostModel();

        if (isset($param['id'])) {
            $id           = $this->request->param('id', 0, 'intval');
            $result       = $portalPostModel->where(['id' => $id])->find();
            $data         = [
                'object_id'   => $result['id'],
                'create_time' => time(),
                'table_name'  => 'portal_post',
                'name'        => $result['post_title'],
                'user_id'=>cmf_get_current_admin_id()
            ];
            $resultPortal = $portalPostModel
                ->where(['id' => $id])
                ->delete();
            if ($resultPortal) {
                Db::name('portal_category_post')->where(['post_id'=>$id])->delete();
                Db::name('portal_tag_post')->where(['post_id'=>$id])->delete();
                Db::name('post_hits')->where(['post_id'=>$id])->delete();
            }
            $this->success("删除成功！", '');

        }

        if (isset($param['ids'])) {
            $ids     = $this->request->param('ids/a');
            $recycle = $portalPostModel->where(['id' => ['in', $ids]])->select();
            $result  = $portalPostModel->where(['id' => ['in', $ids]])->delete();
            if ($result) {
                Db::name('portal_category_post')->where(['post_id' => ['in', $ids]])->delete();
                Db::name('portal_tag_post')->where(['post_id' => ['in', $ids]])->delete();
                Db::name('post_hits')->where(['post_id'=>['in', $ids]])->delete();
                
                $this->success("删除成功！", '');
            }
        }
    }

